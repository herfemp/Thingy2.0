inline int8_t xadd (int8_t x1, int8_t x2) {

	x1 += x2;

	if(x1 < 0 || x1 >= BOARD_WIDTH){
		if(x1 < 0)	while (x1 < 0) x1 += BOARD_WIDTH;
		if(x1 >= BOARD_WIDTH)	while (x1 >= BOARD_WIDTH) x1 -= BOARD_WIDTH;
	}
	return x1;
}

// add to a height index, wrapping around
inline int8_t yadd (int8_t y1, int8_t y2) {

	y1 += y2;

	if(y1 < 0 || y1 >= BOARD_HEIGHT){
		if(y1 < 0) while (y1 < 0) y1 += BOARD_HEIGHT;
		if(y1 >= BOARD_HEIGHT) while (y1 >= BOARD_HEIGHT) y1 -= BOARD_HEIGHT;
	}
	return y1;
}

void play () {

	int8_t	x, y, x2, y2, a;

	while(1){
	for (x=0; x<BOARD_WIDTH; x++) for (y=0; y<BOARD_HEIGHT; y++) {

		a = 0;
		for (x2=-1; x2<=1; x2++) for (y2=-1; y2<=1; y2++)
			if (x2 || y2)
				if (board[xadd(x,x2)][yadd(y,y2)]) a++;

		if      (a == 3) newboard[x][y] = 0xFF;
		else if (a != 2) newboard[x][y] = 0; // Either more than 3 or less than 2


		if(newboard[x][y] != board[x][y])
			ST7735_DrawPixel(x*2, y*2, newboard[x][y]<<5);
	}

	DMA1_Channel6->CNDTR = (BOARD_WIDTH*BOARD_HEIGHT)/4;
	DMA1_Channel6->CPAR = (u32)&newboard;
	DMA1_Channel6->CMAR = (u32) &board;
	DMA1_Channel6->CCR |= DMA_CCR1_EN;

	while(DMA_GetCurrDataCounter(DMA1_Channel6) !=0);
	DMA1_Channel6->CCR &= (uint16_t)(~DMA_CCR1_EN);
	}
}



void Conway() {

	RCC_AHBPeriphClockCmd(RCC_AHBPeriph_DMA1, ENABLE);

    DMA_InitStructure.DMA_BufferSize         =(BOARD_WIDTH*BOARD_HEIGHT)/4;
    DMA_InitStructure.DMA_PeripheralDataSize = DMA_PeripheralDataSize_Word;
    DMA_InitStructure.DMA_MemoryDataSize     = DMA_MemoryDataSize_Word;
    DMA_InitStructure.DMA_PeripheralBaseAddr = (u32)&newboard;
    DMA_InitStructure.DMA_MemoryBaseAddr     = (u32)&board;
    DMA_InitStructure.DMA_PeripheralInc      = DMA_PeripheralInc_Enable;
    DMA_InitStructure.DMA_MemoryInc          = DMA_MemoryInc_Enable;
    DMA_InitStructure.DMA_Priority           = DMA_Priority_High;
    DMA_InitStructure.DMA_Mode               = DMA_Mode_Normal;
    DMA_InitStructure.DMA_DIR                = DMA_DIR_PeripheralDST;
    DMA_InitStructure.DMA_M2M                = DMA_M2M_Enable;
    DMA_DeInit(DMA1_Channel6);
    DMA_Init(DMA1_Channel6, &DMA_InitStructure);

    DMA1_Channel6->CCR |= DMA_CCR1_EN;
	while(DMA_GetCurrDataCounter(DMA1_Channel6) !=0);
	DMA1_Channel6->CCR &= (uint16_t)(~DMA_CCR1_EN);

	DMA_InitStructure.DMA_DIR                = DMA_DIR_PeripheralSRC;
	DMA_Init(DMA1_Channel6, &DMA_InitStructure);

	play ();

}
